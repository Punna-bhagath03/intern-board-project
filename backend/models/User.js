const mongoose = require('mongoose');

const userSchema = new mongoose.Schema({
  username: {
    type: String,
    required: true,
    unique: true,
  },
  password: {
    type: String,
    required: true,
  },
  avatar: {
    type: String, // URL or path to avatar image
    default: '',
  },
});

module.exports = mongoose.model('User', userSchema);
